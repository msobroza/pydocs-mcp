"""R2 overlap check: Live × Pro-Python repo/org intersection (ADR 0013).

Pure over ``(live_records, pro_python_repos)`` — the parquet read happens in
:mod:`download`, so this is deterministic and offline-testable. It computes both the
repo-level and org-level intersections, and the org-level Live exclusion set (ADR 0013
excludes at ORG granularity: any Live instance whose org appears in the frozen test set
is dropped from dev/val — killing the org-idiosyncrasy leakage channel R2 exists to
prevent). The rendered report is a committed artifact re-run on any pin change.
"""

from __future__ import annotations

from collections import Counter
from dataclasses import dataclass

from .records import LiveRecord, org_of


@dataclass(frozen=True, slots=True)
class OverlapReport:
    """Measured R2 intersection + the org-level Live exclusion set."""

    live_repos: int
    live_orgs: int
    pro_python_repos: tuple[str, ...]
    pro_orgs: tuple[str, ...]
    repo_intersection: tuple[str, ...]
    org_intersection: tuple[str, ...]
    # Per-repo counts of the Live instances excluded at org level (e.g. ansible/*).
    excluded_by_repo: tuple[tuple[str, int], ...]
    live_working_instances: int
    live_raw_rows: int
    pro_python_instances: int

    @property
    def excluded_instances(self) -> int:
        return sum(count for _repo, count in self.excluded_by_repo)


def compute_overlap(
    live_records: list[LiveRecord],
    pro_python_repos: list[str],
    *,
    live_raw_rows: int | None = None,
    pro_python_instances: int | None = None,
) -> OverlapReport:
    """Compute the R2 repo/org intersections and the org-level exclusion set.

    ``live_records`` is the POST-dedupe working set; ``live_raw_rows`` (pre-dedupe) and
    ``pro_python_instances`` are threaded through for the report's 1888/266 provenance
    lines (default to the working counts when not supplied).
    """
    live_repos = {r.repo for r in live_records}
    live_orgs = {r.org for r in live_records}
    pro_orgs = {org_of(repo) for repo in pro_python_repos}
    excluded = Counter(r.repo for r in live_records if r.org in pro_orgs)
    return OverlapReport(
        live_repos=len(live_repos),
        live_orgs=len(live_orgs),
        pro_python_repos=tuple(sorted(pro_python_repos)),
        pro_orgs=tuple(sorted(pro_orgs)),
        repo_intersection=tuple(sorted(live_repos & set(pro_python_repos))),
        org_intersection=tuple(sorted(live_orgs & pro_orgs)),
        excluded_by_repo=tuple(sorted(excluded.items())),
        live_working_instances=len(live_records),
        live_raw_rows=live_raw_rows if live_raw_rows is not None else len(live_records),
        pro_python_instances=(pro_python_instances if pro_python_instances is not None else 0),
    )


def excluded_instance_ids(
    live_records: list[LiveRecord],
    pro_python_repos: list[str],
) -> set[str]:
    """Instance IDs removed from dev/val by the org-level R2 exclusion."""
    pro_orgs = {org_of(repo) for repo in pro_python_repos}
    return {r.instance_id for r in live_records if r.org in pro_orgs}


def render_markdown(report: OverlapReport) -> str:
    """Render the committed ``overlap-report.md`` — a re-runnable measured artifact."""
    lines = [
        "# SWE-bench-Live × SWE-bench Pro — R2 overlap report",
        "",
        "Generated by `pydocs_eval.datasets_swe.build_overlap` over the pinned",
        "revisions (ADR 0013). Re-run on any pin change; this file is the measured",
        "R2 artifact, not an assertion.",
        "",
        "## Snapshot shape",
        "",
        f"- Live `full` raw rows: **{report.live_raw_rows}** → working instances "
        f"(post-dedupe): **{report.live_working_instances}**",
        f"- Live distinct repos: **{report.live_repos}** across **{report.live_orgs}** orgs",
        f"- Pro-Python instances: **{report.pro_python_instances}** across "
        f"**{len(report.pro_python_repos)}** repos "
        f"({', '.join(report.pro_python_repos)})",
        "",
        "## Repo-level intersection (R2 at repo granularity)",
        "",
        f"- Live ∩ Pro-Python repos: **{_fmt_set(report.repo_intersection)}**",
        "",
        "## Org-level intersection + exclusion (R2 at ORG granularity — the pinned rule)",
        "",
        f"- Live ∩ Pro orgs: **{_fmt_set(report.org_intersection)}**",
        f"- Live instances excluded from dev/val: **{report.excluded_instances}** "
        f"({_pct(report.excluded_instances, report.live_working_instances)})",
        "",
    ]
    lines.extend(_exclusion_table(report))
    return "\n".join(lines) + "\n"


def _exclusion_table(report: OverlapReport) -> list[str]:
    if not report.excluded_by_repo:
        return ["No org-level exclusions.", ""]
    rows = ["| Excluded Live repo | Instances |", "|---|---|"]
    rows.extend(f"| `{repo}` | {count} |" for repo, count in report.excluded_by_repo)
    rows.append("")
    return rows


def _fmt_set(items: tuple[str, ...]) -> str:
    return "∅ (empty)" if not items else ", ".join(f"`{i}`" for i in items)


def _pct(part: int, whole: int) -> str:
    return "0%" if whole == 0 else f"{100 * part / whole:.2f}%"
